// Nível Fácil

// Soma dois números
export const add = (a, b) => {
  return a + b;
}

// Subtrai dois números
export const subtract = (a, b) => {
  return a - b;
}

// Multiplica dois números
export const multiply = (a, b) => {
  return a * b;
}

// Divide dois números
export const divide = (a, b) => {
  return a / b;
}
